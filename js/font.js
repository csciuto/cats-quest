// Draw a single character; returns advance width (always 6)
function dC(c,x,y,col){
  const g=GL[c.toUpperCase()]||GL[" "];
  X.fillStyle=col;
  for(let ry=0;ry<g.length;ry++)
    for(let rx=0;rx<g[ry].length;rx++)
      if(g[ry][rx])X.fillRect(x+rx,y+ry,1,1);
  return 6;
}

// Draw a string
function dT(t,x,y,c){let p=x;for(const ch of t)p+=dC(ch,p,y,c);}

// Measure pixel width of a string
function tW(t){return t.length*6;}

// Word-wrap text to maxWidth pixels
function wrap(t,mw){
  const w=t.split(" "),ls=[];let l="";
  for(const s of w){
    const n=l?l+" "+s:s;
    if(tW(n)>mw&&l){ls.push(l);l=s;}else l=n;
  }
  if(l)ls.push(l);
  return ls;
}
